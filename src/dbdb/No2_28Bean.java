package bean;

public class No2_28Bean {
	private String name;
	private String mail;
	private String naiyo;



	public No2_28Bean(String name , String mail , String naiyo ) {
		super();
		this.name = name;
		this.mail = mail;
		this.naiyo = naiyo;
	}


	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getMail() {
		return mail;
	}

	public void setMail(String mail) {
		this.mail = mail;
	}

	public String getNaiyo() {
		return naiyo;
	}

	public void setIsbn(String naiyo) {
		this.naiyo = naiyo;
	}



}